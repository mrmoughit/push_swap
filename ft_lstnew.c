#include "push_swap.h"

t_list	*ft_lstnew(int content)
{
	t_list	*p;

	p = malloc(sizeof(t_list));
	if (!p)
		return (NULL);
	p->content = content;
	p->next = NULL;
	p->tar = NULL;
	p->moves = 0;
	p->flag = 0;
	return (p);
}
